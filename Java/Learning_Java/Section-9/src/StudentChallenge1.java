public class StudentChallenge1 {
    public static void main(String[] args) {
        // 1. Finding sum of all elements
        int[] B = {2,4,6,8,10};
        int sum =0;
        for (int i = 0; i < B.length; i++) {
            sum+=B[i];
        }
        System.out.println(sum);
    }
}
